#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	result;

	sign = 1;
	result = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		sign -= (*str++ == '-') * 2;
	while (ft_isdigit(*str))
		result = *str++ - '0' + (result * 10);
	return (result * sign);
}
